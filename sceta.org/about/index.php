					<h3>Innovative Tech Expo</h3>

					<p>
						Since 2014, SCETA has aimed higher to give our student engineers bigger and better with the Innovative
						Tech Expo where both members and non-members alike can see for themselves a world of technology that
						yields endless possibilities annually during the Spring academic quarter. As a child of the Alternative
						Vehicles Exposition from years past, this inspiring event aims to showcase some of the world's cutting 
						edge technology that expands beyond alternatives vehicles into a world of 3D printing, robotics, and 
