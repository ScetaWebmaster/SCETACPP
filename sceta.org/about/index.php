					<h3>Alternative Vehicles Exposition</h3>

					<p>
						Since its inception in 2011, SCETA has been hosting the Alternative Vehicles Exposition on the
						Cal Poly Pomona campus to spread awareness about the growing industry of alternative vehicles
						specializing in hybrid, all-electric, fuel-cell, and solar-cell technology, just to name a few.
						We aspire to inspire our student engineers about the possibilities and the viability of alternative
